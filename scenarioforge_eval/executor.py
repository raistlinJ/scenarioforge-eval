import os
import sys
import uuid
import json
import traceback

# Make sure scenarioforge is in path
sys.path.insert(0, os.path.abspath(os.path.join(os.path.dirname(__file__), '../../scenarioforge')))
from webapp import app_backend as backend
from scenarioforge.cli import main as sf_cli_main

class Executor:
    def __init__(self, spec: dict, out_dir: str):
        self.spec = spec
        self.out_dir = out_dir
        os.makedirs(self.out_dir, exist_ok=True)

    def _generate_xml(self) -> str:
        """Uses the UI's XML generator to build a random topology XML."""
        # Translate spec to the payload expected by _build_scenarios_xml
        topo_spec = self.spec.get('topology', {})
        scenarios_inline = [{
            'name': self.spec.get('name', 'eval'),
            'nodes': self._generate_nodes(topo_spec),
            'links': [], # Links will be auto-generated by cli.py if we just supply nodes
        }]
        
        # Build XML
        tree = backend._build_scenarios_xml({'scenarios': scenarios_inline})
        xml_path = os.path.join(self.out_dir, 'scenario.xml')
        tree.write(xml_path, encoding='utf-8', xml_declaration=True)
        return xml_path

    def _generate_nodes(self, topo_spec: dict) -> list:
        nodes = []
        node_id = 1
        num_routers = topo_spec.get('routers', 2)
        num_hosts = topo_spec.get('hosts', 5)
        
        for _ in range(num_routers):
            nodes.append({"id": node_id, "name": f"router-{node_id}", "type": "router"})
            node_id += 1
            
        for _ in range(num_hosts):
            nodes.append({"id": node_id, "name": f"host-{node_id}", "type": "docker"})
            node_id += 1
            
        return nodes

    def run(self):
        result = {
            'success': False,
            'stages': {},
            'error': None
        }
        
        try:
            # 1. Generate XML Topology
            xml_path = self._generate_xml()
            result['stages']['topology'] = 'PASS'
            
            # 2. Run Preview/Execution via CLI
            # We mock sys.argv to drive the CLI
            sys.argv = [
                'scenarioforge',
                '--xml', xml_path,
                '--preview-full',
                '--plan-output', os.path.join(self.out_dir, 'plan.json'),
                '--verbose'
            ]
            
            # TODO: We would run the full execution here, but we start with --preview-full
            # to validate the generation and Flag Sequence pipeline first.
            try:
                sf_cli_main()
                result['stages']['preview'] = 'PASS'
                result['success'] = True
            except SystemExit as e:
                if e.code == 0:
                    result['stages']['preview'] = 'PASS'
                    result['success'] = True
                else:
                    raise RuntimeError(f"CLI exited with code {e.code}")
                    
        except Exception as e:
            result['error'] = traceback.format_exc()
            result['stages']['failed_at'] = str(e)
            
        return result
