import os
import sys
import uuid
import json
import traceback

class Executor:
    def __init__(self, spec: dict, out_dir: str, sf_path: str, execute: bool = False, verbose: bool = False):
        self.spec = spec
        self.out_dir = out_dir
        self.sf_path = os.path.abspath(sf_path)
        self.execute = execute
        self.verbose = verbose
        os.makedirs(self.out_dir, exist_ok=True)
        
        # Dynamically add scenarioforge to the path
        if self.sf_path not in sys.path:
            sys.path.insert(0, self.sf_path)

    def _generate_xml(self) -> str:
        """Uses the UI's XML generator to build a random topology XML."""
        from webapp import app_backend as backend
        # Translate spec to the payload expected by _build_scenarios_xml
        topo_spec = self.spec.get('topology', {})
        scen_payload = {
            'name': self.spec.get('name', 'eval'),
            'nodes': self._generate_nodes(topo_spec),
            'links': [], # Links will be auto-generated by cli.py if we just supply nodes
            'sections': {},
        }
        
        # Inject vulnerabilities count into sections
        vulns_spec = self.spec.get('vulns', {})
        if vulns_spec.get('randomize'):
            count = vulns_spec.get('count', 1)
            density = min(1.0, count / 10.0)
            scen_payload['sections']['Vulnerabilities'] = {
                'density': density,
                'items': [{'selected': 'Random', 'v_metric': 'Weight', 'factor': 1.0}]
            }
            
        # Inject services count into sections
        services_spec = self.spec.get('services', {})
        if services_spec.get('randomize'):
            scen_payload['sections']['Services'] = {
                'items': [{'v_metric': 'Count', 'v_count': 3}] # Default to 3 random services
            }
            
        # Inject flow_state
        flows_spec = self.spec.get('flows', {})
        if flows_spec.get('randomize'):
            scen_payload['flow_state'] = {
                'auto_chain': True,
                'chain_length': flows_spec.get('chain_length', 3),
                'allow_node_duplicates': flows_spec.get('allow_duplicates', False)
            }
            
        # Inject Segmentation
        seg_spec = self.spec.get('segmentation', {})
        if seg_spec.get('randomize'):
            scen_payload['sections']['Segmentation'] = {
                'density_input': seg_spec.get('density', 0.5)
            }
            
        # Load environment variables (CORE_HOST, SSH credentials, etc) using scenarioforge's native loader
        from webapp.env_loader import load_runtime_env_files
        from pathlib import Path
        load_runtime_env_files(base_dir=Path(self.sf_path))
        
        # Enforce VM mode for scenarioforge-eval
        webui_mode = os.environ.get('CORETG_WEBUI_MODE', 'native').lower()
        if webui_mode != 'vm':
            raise RuntimeError(
                f"scenarioforge-eval only supports 'vm' mode. Your .scenarioforge.env "
                f"is currently set to CORETG_WEBUI_MODE='{webui_mode}'. Please update "
                f"it to 'vm' to run the evaluation."
            )
        
        # Inject HITL
        hitl_spec = self.spec.get('hitl', {})
        if hitl_spec.get('use_env'):
            hitl_enabled = str(os.environ.get('CORETG_VM_MODE_HITL_ENABLED', '')).lower() in ('true', '1', 'yes')
            hitl_iface = os.environ.get('CORETG_VM_MODE_HITL_CORE_IFX_NAME')
            hitl_attachment = os.environ.get('CORETG_VM_MODE_HITL_CORE_IFX_ATTACHMENT')
                            
            if hitl_enabled and hitl_iface:
                scen_payload['hitl'] = {
                    'enabled': True,
                    'interfaces': [
                        {'name': hitl_iface, 'attachment': hitl_attachment or 'existing_router'}
                    ]
                }
            
        scenarios_inline = [scen_payload]
        
        # Build XML
        tree = backend._build_scenarios_xml({'scenarios': scenarios_inline})
        xml_path = os.path.join(self.out_dir, 'scenario.xml')
        tree.write(xml_path, encoding='utf-8', xml_declaration=True)
        return xml_path

    def _generate_nodes(self, topo_spec: dict) -> list:
        nodes = []
        node_id = 1
        num_routers = topo_spec.get('routers', 2)
        num_hosts = topo_spec.get('hosts', 5)
        
        for _ in range(num_routers):
            nodes.append({"id": node_id, "name": f"router-{node_id}", "type": "router"})
            node_id += 1
            
        for _ in range(num_hosts):
            nodes.append({"id": node_id, "name": f"host-{node_id}", "type": "docker"})
            node_id += 1
            
        return nodes

    def run(self):
        result = {
            'success': False,
            'stages': {},
            'error': None
        }
        
        try:
            print(">> Phase: topology gen")
            print(">> Phase: flow-generation")
            # 1. Generate XML Topology
            xml_path = self._generate_xml()
            result['stages']['topology'] = 'PASS'
            
            if not self.execute:
                print(">> Phase: preview gen")
                # 2. Run Preview via CLI (Local)
                # We mock sys.argv to drive the CLI
                from scenarioforge.cli import main as sf_cli_main
                
                sys.argv = [
                    'scenarioforge',
                    '--xml', xml_path,
                    '--plan-output', os.path.join(self.out_dir, 'plan.json'),
                    '--preview-full'
                ]
                
                if self.verbose:
                    sys.argv.append('--verbose')
                
                # Change working directory so any generated artifacts (e.g. docker-compose.yml) 
                # are dumped directly into this scenario's out_dir
                original_cwd = os.getcwd()
                os.chdir(self.out_dir)
                
                # Monkeypatch the report writer to redirect reports into this output directory 
                # instead of trying to write them into the scenarioforge codebase's reports/ folder
                import scenarioforge.cli
                original_write_report = scenarioforge.cli.write_report
                
                def mocked_write_report(report_path, *args, **kwargs):
                    new_report_path = os.path.join(self.out_dir, os.path.basename(report_path))
                    return original_write_report(new_report_path, *args, **kwargs)
                    
                scenarioforge.cli.write_report = mocked_write_report
                
                try:
                    sf_cli_main()
                    result['stages']['preview'] = 'PASS'
                    result['success'] = True
                except SystemExit as e:
                    if e.code == 0:
                        result['stages']['preview'] = 'PASS'
                        result['success'] = True
                    else:
                        raise RuntimeError(f"CLI exited with code {e.code}")
                finally:
                    os.chdir(original_cwd)
                    scenarioforge.cli.write_report = original_write_report
            else:
                print(">> Phase: preview gen")
                print(">> Phase: execute")
                # 2. Run Execution via SSH (Remote)
                import uuid
                from webapp.app_backend import (
                    _core_backend_defaults,
                    _open_ssh_client,
                    _exec_ssh_command,
                    _push_repo_to_remote,
                )
                
                print(f"Deploying scenario {self.spec.get('name', 'unknown')} to remote VM...")
                core_cfg = _core_backend_defaults(include_password=True)
                
                # Step 2a: Push the codebase to remote
                print("  Syncing codebase to remote VM...")
                repo_sync = _push_repo_to_remote(core_cfg, upload_only_injected_artifacts=False)
                remote_repo = repo_sync.get('repo_path')
                if not remote_repo:
                    raise RuntimeError("Failed to sync codebase to remote VM: repo_path not returned")
                
                # Step 2b: Push the mock XML via SFTP
                print("  Pushing mock XML topology to remote VM...")
                client = _open_ssh_client(core_cfg)
                sftp = client.open_sftp()
                
                remote_xml = f"/tmp/scenarioforge_eval_mock_{uuid.uuid4().hex[:8]}.xml"
                sftp.put(xml_path, remote_xml)
                sftp.close()
                
                # Step 2c: Execute CLI on remote VM
                print("  Executing scenario via SSH (this may take 5-10 minutes)...")
                # Find correct python interpreter (including scenarioforge's local .venv)
                cmd = (
                    f"cd {remote_repo} && "
                    f"PYTHON=\"\"; "
                    f"for py in {self.sf_path}/.venv/bin/python {remote_repo}/.venv/bin/python core-python /opt/core/venv/bin/python python3 python; do "
                    f"  if command -v $py >/dev/null 2>&1; then "
                    f"    if $py -c 'import core.api.grpc' 2>/dev/null; then PYTHON=$py; break; fi; "
                    f"  fi; "
                    f"done; "
                    f"if [ -z \"$PYTHON\" ]; then echo \"ERROR: Could not find Python interpreter with core.api.grpc on remote VM!\" >&2; exit 1; fi; "
                    f"$PYTHON -m scenarioforge.cli --xml {remote_xml} "
                    f"--plan-output /tmp/scenarioforge_eval_plan.json"
                )
                if self.verbose:
                    cmd += " --verbose"
                    
                stdin, stdout, stderr = client.exec_command(cmd)
                
                # We stream stderr line-by-line because Python's logging (where PHASE: prints) goes to stderr
                # We will capture it all for the final string, but print relevant lines immediately.
                import select
                channel = stdout.channel
                stderr_lines = []
                stdout_lines = []
                
                while not channel.exit_status_ready():
                    r, _, _ = select.select([channel, channel.stderr], [], [], 1.0)
                    if channel in r and channel.recv_ready():
                        chunk = channel.recv(4096).decode('utf-8', errors='replace')
                        if chunk:
                            stdout_lines.append(chunk)
                            if self.verbose:
                                print(chunk, end="", flush=True)
                    
                    if channel.stderr in r and channel.recv_stderr_ready():
                        chunk = channel.recv_stderr(4096).decode('utf-8', errors='replace')
                        if chunk:
                            stderr_lines.append(chunk)
                            # Print immediately if it's a PHASE or verbose
                            lines = chunk.split('\n')
                            for line in lines:
                                if "PHASE:" in line or self.verbose:
                                    print(line)
                
                code = channel.recv_exit_status()
                
                # Drain remaining output
                if channel.recv_ready():
                    stdout_lines.append(channel.recv(4096).decode('utf-8', errors='replace'))
                if channel.recv_stderr_ready():
                    stderr_lines.append(channel.recv_stderr(4096).decode('utf-8', errors='replace'))
                    
                full_stdout = "".join(stdout_lines)
                full_stderr = "".join(stderr_lines)
                
                # Clean up XML
                client.exec_command(f"rm -f {remote_xml}")
                
                if code == 0:
                    result['stages']['preview'] = 'PASS'
                    result['stages']['execute'] = 'PASS'
                    result['success'] = True
                    print(f"  Successfully deployed scenario!")
                else:
                    print(f"  Execution failed with code {code}!")
                    if not self.verbose: # If verbose, we already printed it
                        print(f"\n--- REMOTE STDOUT ---\n{full_stdout}")
                        print(f"\n--- REMOTE STDERR ---\n{full_stderr}")
                    raise RuntimeError(f"Remote execution failed with exit code {code}")
                    
        except Exception as e:
            result['error'] = traceback.format_exc()
            result['stages']['failed_at'] = str(e)
            
        return result
